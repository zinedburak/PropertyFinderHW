package main

func main() {
	// Plus
	x := 0
	// All three add one to the value of x
	x = x + 1
	x += 1
	x++

	// Minus
	y := 0

	// All three subtract 1 from the value of y
	y = y - 1
	y -= 1
	y--

	// Multiplication
	// Both multiply x with 2
	x = x * 2
	x *= 2

	// Division
	// Both divide y by 2
	y = y / 2
	y /= 2

	// Mod
	//Both  Divide X by 10 and take the reminder
	x = x % 10
	x %= 10
}
